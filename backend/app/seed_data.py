def get_roi_scenarios() -> ROIScenariosResponse:
    def build_projections(
        initial: float,
        annual_savings_start: float,
        savings_growth: float,
        years: int,
        start_year: int = 2026,
    ) -> list[AnnualROIProjection]:
        projections = []
        cumulative_investment = initial
        cumulative_savings = 0.0
        for i in range(years):
            year = start_year + i
            annual_savings = annual_savings_start * ((1 + savings_growth) ** i)
            cumulative_savings += annual_savings
            net = cumulative_savings - cumulative_investment
            roi = (net / cumulative_investment) * 100 if cumulative_investment > 0 else 0
            projections.append(AnnualROIProjection(
                year=year,
                cumulative_investment_usd_k=cumulative_investment,
                cumulative_savings_usd_k=round(cumulative_savings, 1),
                net_position_usd_k=round(net, 1),
                roi_pct=round(roi, 1),
            ))
        return projections

